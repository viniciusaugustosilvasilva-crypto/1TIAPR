print(True and False)  # E
print(True or False)   # OU
print(not False)        # NÃO

nome = "João"
nome  ==  "João"  # Igualdade
print(nome  !=  "Maria")  # Diferença
print(nome == "João")  # Igualdade

idade = 20
print("Idade:", idade)
print(idade == 18)  # Igualdade
print(idade > 18)   # Maior que
print(idade < 18)   # Menor que
print(idade >= 18)  # Maior ou igual a
print(idade <= 18)  # Menor ou igual a